def _print_latency_table(scenarios, labels, key, endpoints, title, stat_key=None):
    """Generic helper to print cold or warm latency comparison tables."""
    print(f"\n  {BOLD}{title}{RESET}\n")
    is_warm = stat_key is not None
    header = f"  {'Endpoint':<25s}"
    for lbl in labels:
        col = lbl + "·" + stat_key if is_warm else lbl
        header += f"  {col:>8s}"
    header += f"  {'B vs A':>8s}  {'A vs C':>8s}  {'B vs C':>8s}"
    print(header)
    print(f"  {SEP}")

    totals = {lbl: 0 for lbl in labels}
    counts = {lbl: 0 for lbl in labels}
    for ep_label, _ in endpoints:
        row = f"  {ep_label:<25s}"
        vals = {}
        for lbl in labels:
            s = scenarios.get(lbl)
            if s and ep_label in s.get(key, {}):
                v = s[key][ep_label] if not is_warm else s[key][ep_label][stat_key]
                vals[lbl] = v
                totals[lbl] += s[key][ep_label]["mean"] if is_warm else v
                counts[lbl] += 1
                row += f"  {v:>6d}ms"
            else:
                row += f"  {'N/A':>8s}"
        for num, den in [("A", "B"), ("C", "A"), ("C", "B")]:
            if num in vals and den in vals and vals[den] > 0:
                ratio = vals[num] / vals[den]
                color = GREEN if ratio > 1.5 else (YELLOW if ratio > 1.0 else RED)
                row += f"  {color}{ratio:>6.1f}x{RESET}"
            else:
                row += f"  {'N/A':>8s}"
        print(row)

    print(f"  {SEP}")
    avg_label = "AVERAGE (mean)" if is_warm else "AVERAGE"
    avg_row = f"  {avg_label:<25s}"
    avgs = {}
    for lbl in labels:
        if counts[lbl] > 0:
            avg = totals[lbl] // counts[lbl]
            avgs[lbl] = avg
            avg_row += f"  {avg:>6d}ms"
        else:
            avg_row += f"  {'N/A':>8s}"
    for num, den in [("A", "B"), ("C", "A"), ("C", "B")]:
        if num in avgs and den in avgs and avgs[den] > 0:
            ratio = avgs[num] / avgs[den]
            color = GREEN if ratio > 1.5 else (YELLOW if ratio > 1.0 else RED)
            avg_row += f"  {color}{ratio:>6.1f}x{RESET}"
        else:
            avg_row += f"  {'N/A':>8s}"
    print(avg_row)


def print_comparison(scenarios):
    section("COMPARISON REPORT — BACKEND (direct snowplow)")

    labels = ["A", "B", "C", "D"]
    desc = {
        "A": "cache ON  + NO clientconfig",
        "B": "cache ON  + clientconfig",
        "C": "cache OFF + NO clientconfig",
        "D": "cache OFF + clientconfig",
    }

    _print_latency_table(scenarios, labels, "cold", ENDPOINTS,
                         "COLD LATENCY — backend (first request after restart, ms)")
    _print_latency_table(scenarios, labels, "warm", ENDPOINTS,
                         f"WARM LATENCY — backend (steady-state p50, {ITERS} iters, ms)",
                         stat_key="p50")

    # ── UI navigation tables ─────────────────────────────────────────────────
    section("COMPARISON REPORT — UI NAVIGATION")
    _print_latency_table(scenarios, labels, "ui_cold", UI_PAGES,
                         "COLD LATENCY — UI pages (first load after restart, ms)")
    _print_latency_table(scenarios, labels, "ui_warm", UI_PAGES,
                         f"WARM LATENCY — UI pages (steady-state p50, {ITERS} iters, ms)",
                         stat_key="p50")

    # ── Analysis ──────────────────────────────────────────────────────────────
    section("ANALYSIS & FINDINGS")
    findings = []

    a_cold = scenarios.get("A", {}).get("cold", {})
    b_cold = scenarios.get("B", {}).get("cold", {})
    if a_cold and b_cold:
        a_t = sum(a_cold.values()); b_t = sum(b_cold.values())
        if b_t > 0:
            r = a_t / b_t
            if r > 2.0:
                findings.append((GREEN, f"[backend] L1 warmup delivers {r:.1f}x cold-start speedup (A={a_t}ms vs B={b_t}ms)"))
            elif r > 1.2:
                findings.append((YELLOW, f"[backend] L1 warmup delivers modest {r:.1f}x cold-start speedup (A={a_t}ms vs B={b_t}ms)"))
            else:
                findings.append((RED, f"[backend] L1 warmup shows NO cold-start improvement ({r:.1f}x) — investigate"))

    a_warm = scenarios.get("A", {}).get("warm", {})
    c_warm = scenarios.get("C", {}).get("warm", {})
    if a_warm and c_warm:
        a_a = sum(v["mean"] for v in a_warm.values()) / max(len(a_warm), 1)
        c_a = sum(v["mean"] for v in c_warm.values()) / max(len(c_warm), 1)
        if a_a > 0:
            r = c_a / a_a
            if r > 1.5:
                findings.append((GREEN, f"[backend] Cache delivers {r:.1f}x warm-latency speedup (A={a_a:.0f}ms vs C={c_a:.0f}ms)"))
            else:
                findings.append((RED, f"[backend] Cache shows minimal warm-latency improvement ({r:.1f}x) — investigate"))

    c_w = scenarios.get("C", {}).get("warm", {})
    d_w = scenarios.get("D", {}).get("warm", {})
    if c_w and d_w:
        c_a = sum(v["mean"] for v in c_w.values()) / max(len(c_w), 1)
        d_a = sum(v["mean"] for v in d_w.values()) / max(len(d_w), 1)
        diff = abs(c_a - d_a) / max(c_a, 1) * 100
        if diff < 15:
            findings.append((GREEN, f"[backend] clientconfig has no impact without cache (C={c_a:.0f}ms vs D={d_a:.0f}ms, Δ={diff:.0f}%)"))
        else:
            findings.append((YELLOW, f"[backend] clientconfig affects latency without cache (Δ={diff:.0f}%) — investigate"))

    b_warm = scenarios.get("B", {}).get("warm", {})
    if a_warm and b_warm:
        a_a = sum(v["mean"] for v in a_warm.values()) / max(len(a_warm), 1)
        b_a = sum(v["mean"] for v in b_warm.values()) / max(len(b_warm), 1)
        diff = abs(a_a - b_a) / max(a_a, 1) * 100
        if diff < 20:
            findings.append((GREEN, f"[backend] Warm latencies similar A vs B (A={a_a:.0f}ms vs B={b_a:.0f}ms, Δ={diff:.0f}%)"))
        else:
            findings.append((YELLOW, f"[backend] Warm latencies differ A vs B (A={a_a:.0f}ms vs B={b_a:.0f}ms, Δ={diff:.0f}%) — investigate"))

    if a_warm and c_warm:
        regressions = []
        for ep_label, _ in ENDPOINTS:
            a_p = a_warm.get(ep_label, {}).get("p50", 0)
            c_p = c_warm.get(ep_label, {}).get("p50", 0)
            if a_p > 0 and c_p > 0 and a_p > c_p * 1.1:
                regressions.append(f"{ep_label} (cached={a_p}ms > nocache={c_p}ms)")
        if regressions:
            findings.append((RED, "[backend] REGRESSION: Cache SLOWER than no-cache for: " + ", ".join(regressions)))
        else:
            findings.append((GREEN, "[backend] No regressions: cache is faster or equal for all endpoints"))

    if b_cold:
        slow = [(ep, ms) for ep, ms in b_cold.items() if ms > 500]
        if slow:
            findings.append((YELLOW, f"[backend] Cold > 500ms even with L1 warmup: {', '.join(f'{e}={m}ms' for e,m in slow)}"))
        else:
            findings.append((GREEN, "[backend] All cold-start endpoints < 500ms with L1 warmup"))

    # UI-specific analysis
    a_ui_warm = scenarios.get("A", {}).get("ui_warm", {})
    b_ui_warm = scenarios.get("B", {}).get("ui_warm", {})
    c_ui_warm = scenarios.get("C", {}).get("ui_warm", {})
    if a_ui_warm and c_ui_warm:
        a_a = sum(v["mean"] for v in a_ui_warm.values()) / max(len(a_ui_warm), 1)
        c_a = sum(v["mean"] for v in c_ui_warm.values()) / max(len(c_ui_warm), 1)
        if a_a > 0:
            r = c_a / a_a
            if r > 1.3:
                findings.append((GREEN, f"[UI] Cache delivers {r:.1f}x UI navigation speedup (A={a_a:.0f}ms vs C={c_a:.0f}ms)"))
            else:
                findings.append((YELLOW, f"[UI] Cache shows limited UI navigation speedup ({r:.1f}x) — UI overhead dominates"))

    if b_ui_warm and a_ui_warm:
        for ep_label, _ in UI_PAGES:
            bv = b_ui_warm.get(ep_label, {}).get("p50", 0)
            av = a_ui_warm.get(ep_label, {}).get("p50", 0)
            if bv > 0 and av > 0:
                overhead = bv - av
                if overhead > 200:
                    findings.append((YELLOW, f"[UI] {ep_label}: L1 warmup saves only {av - bv}ms on UI side (B={bv}ms vs A={av}ms)"))

    # Compare UI overhead vs backend
    if b_warm and b_ui_warm:
        dash_backend = b_warm.get("page/dashboard", {}).get("p50", 0)
        dash_ui = b_ui_warm.get("ui/dashboard", {}).get("p50", 0)
        if dash_backend > 0 and dash_ui > 0:
            overhead_ms = dash_ui - dash_backend
            findings.append((CYAN, f"[UI] UI proxy overhead for dashboard: {overhead_ms:+d}ms (UI={dash_ui}ms vs backend={dash_backend}ms)"))

    print()
    for color, finding in findings:
        icon = "✓" if color == GREEN else ("⚠" if color == YELLOW else ("→" if color == CYAN else "✗"))
        print(f"  {color}{icon}{RESET} {finding}")
    print()

    print(f"  {BOLD}SCENARIO SUMMARY{RESET}")
    print(f"  {SEP}")
    for lbl in labels:
        s = scenarios.get(lbl)
        if not s:
            print(f"  {lbl}: {desc[lbl]:<40s}  SKIPPED"); continue
        ca = sum(s["cold"].values()) // max(len(s["cold"]), 1)
        wa = sum(v["mean"] for v in s["warm"].values()) // max(len(s["warm"]), 1)
        ui_ca = sum(s.get("ui_cold", {}).values()) // max(len(s.get("ui_cold", {})), 1) if s.get("ui_cold") else 0
        ui_wa = sum(v["mean"] for v in s.get("ui_warm", {}).values()) // max(len(s.get("ui_warm", {})), 1) if s.get("ui_warm") else 0
        print(f"  {lbl}: {desc[lbl]:<40s}  backend cold={ca:>5d}ms warm={wa:>5d}ms  |  UI cold={ui_ca:>5d}ms warm={ui_wa:>5d}ms")
    print(f"  {SEP}\n")


def print_lifecycle_report(phases):
    section("LIFECYCLE SCENARIO REPORT (Scenario E)")

    print(f"\n  {BOLD}{'Phase':<40s}  {'backend':>22s}  │  {'UI navigation':>22s}{RESET}")
    print(f"  {BOLD}{'':<40s}  {'dashboard':>10s} {'comp-list':>10s}  │  {'dashboard':>10s} {'comps':>10s}{RESET}")
    print(f"  {SEP}")
    for p in phases:
        dc = GREEN if p["dashboard_code"] == 200 else RED
        cc = GREEN if p["complist_code"] == 200 else RED
        udc = GREEN if p.get("ui_dashboard_code", 0) in range(200, 400) else RED
        ucc = GREEN if p.get("ui_compositions_code", 0) in range(200, 400) else RED
        ui_d = p.get("ui_dashboard_ms", 0)
        ui_c = p.get("ui_compositions_ms", 0)
        print(f"  {p['label']:<40s}  "
              f"{dc}{p['dashboard_ms']:>8d}ms{RESET} "
              f"{cc}{p['complist_ms']:>8d}ms{RESET}  │  "
              f"{udc}{ui_d:>8d}ms{RESET} "
              f"{ucc}{ui_c:>8d}ms{RESET}")
    print(f"  {SEP}")

    print(f"\n  {BOLD}LIFECYCLE ANALYSIS{RESET}\n")
    findings = []

    if len(phases) >= 6:
        all_ok = all(p["dashboard_code"] == 200 for p in phases)
        if all_ok:
            findings.append((GREEN, "[backend] Dashboard returned HTTP 200 in all phases"))
        else:
            bad = [p["label"] for p in phases if p["dashboard_code"] != 200]
            findings.append((RED, f"[backend] Dashboard failed in: {', '.join(bad)}"))

        p0_d, p4_d, p5_d = phases[0]["dashboard_ms"], phases[4]["dashboard_ms"], phases[5]["dashboard_ms"]
        findings.append((CYAN, f"[backend] Clean slate → {p0_d}ms,  after 20 compositions → {p4_d}ms"))

        if p5_d < p4_d * 2:
            findings.append((GREEN, f"[backend] Dashboard responsive after deletion ({p5_d}ms vs {p4_d}ms)"))
        else:
            findings.append((YELLOW, f"[backend] Dashboard slower after deletion ({p5_d}ms vs {p4_d}ms) — cache invalidation may be slow"))

        dash_times = [p["dashboard_ms"] for p in phases]
        mx = max(dash_times)
        if mx < 5000:
            findings.append((GREEN, f"[backend] Dashboard stayed under 5s across all phases (max={mx}ms)"))
        elif mx < 10000:
            findings.append((YELLOW, f"[backend] Dashboard peaked at {mx}ms — acceptable but room for improvement"))
        else:
            findings.append((RED, f"[backend] Dashboard peaked at {mx}ms — too slow"))

        # UI lifecycle analysis
        ui_dash_times = [p.get("ui_dashboard_ms", 0) for p in phases]
        if any(t > 0 for t in ui_dash_times):
            ui_mx = max(ui_dash_times)
            if ui_mx < 5000:
                findings.append((GREEN, f"[UI] Dashboard page stayed under 5s across all phases (max={ui_mx}ms)"))
            elif ui_mx < 10000:
                findings.append((YELLOW, f"[UI] Dashboard page peaked at {ui_mx}ms — acceptable but room for improvement"))
            else:
                findings.append((RED, f"[UI] Dashboard page peaked at {ui_mx}ms — too slow"))

            avg_overhead = sum(
                p.get("ui_dashboard_ms", 0) - p["dashboard_ms"]
                for p in phases if p.get("ui_dashboard_ms", 0) > 0
            ) // max(sum(1 for p in phases if p.get("ui_dashboard_ms", 0) > 0), 1)
            findings.append((CYAN, f"[UI] Average UI overhead vs backend for dashboard: {avg_overhead:+d}ms"))

    print()
    for color, finding in findings:
        icon = "✓" if color == GREEN else ("⚠" if color == YELLOW else ("→" if color == CYAN else "✗"))
        print(f"  {color}{icon}{RESET} {finding}")
    print()


