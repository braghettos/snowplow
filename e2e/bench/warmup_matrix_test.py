def wait_for_compdef_ready(timeout=300):
    log(f"Waiting up to {timeout}s for CompositionDefinition to become Ready...")
    deadline = time.time() + timeout
    while time.time() < deadline:
        rc, out, _ = kubectl(
            "get", "compositiondefinitions.core.krateo.io",
            COMPDEF_NAME, "-n", "bench-ns-01",
            "-o", "jsonpath={.status.conditions[?(@.type=='Ready')].status}")
        if rc == 0 and out.strip() == "True":
            log("CompositionDefinition is Ready")
            return True
        time.sleep(5)
    log("WARNING: CompositionDefinition not Ready within timeout")
    return False


def wait_for_crd_gone(timeout=120):
    log(f"Waiting up to {timeout}s for CRD to be removed...")
    deadline = time.time() + timeout
    while time.time() < deadline:
        rc, _, _ = kubectl("get", "crd",
                           f"{COMP_RES}.{COMP_GVR}", "--no-headers")
        if rc != 0:
            log("CRD removed")
            return True
        time.sleep(5)
    log("WARNING: CRD still present after timeout")
    return False


def run_lifecycle_scenario(token):
    """
    Scenario E — Incremental resource lifecycle with cache ENABLED.

    Phases:
      0. Clean slate (zero compositiondefs, zero bench-ns, zero compositions)
      1. Create first bench namespace
      2. Create the CompositionDefinition (waits for CRD to appear)
      3. Create remaining 19 bench namespaces
      4. Create 20 compositions (one per namespace)
      5. Delete 1 composition
    """
    section("Scenario E — Incremental Resource Lifecycle (cache ENABLED)")
    phases = []
    informer_settle = 10

    # ── Phase 0: clean slate ──────────────────────────────────────────────────
    log("Phase 0: establishing clean slate...")

    rc, out, _ = kubectl("get", f"{COMP_RES}.{COMP_GVR}", "-A",
                         "--no-headers", "-o",
                         "custom-columns=NS:.metadata.namespace,NAME:.metadata.name")
    if rc == 0 and out.strip():
        for line in out.strip().split("\n"):
            parts = line.split()
            if len(parts) == 2:
                kubectl("delete", f"{COMP_RES}.{COMP_GVR}", parts[1],
                        "-n", parts[0], "--ignore-not-found", "--wait=false")
        log("Composition delete requests sent, waiting 30s for finalizers...")
        time.sleep(30)

    kubectl("delete", "compositiondefinitions.core.krateo.io",
            COMPDEF_NAME, "-n", "bench-ns-01",
            "--ignore-not-found", "--wait=false")
    log("CompositionDefinition delete requested")
    time.sleep(10)
    wait_for_crd_gone(timeout=180)

    for i in range(1, BENCH_NS_COUNT + 1):
        kubectl("delete", "ns", f"bench-ns-{i:02d}", "--ignore-not-found",
                "--wait=false")
    log(f"Deleted {BENCH_NS_COUNT} bench namespaces (async)")
    log("Waiting 30s for namespace cleanup...")
    time.sleep(30)

    rc, out, _ = kubectl("get", "ns", "-o", "name")
    remaining = [l for l in out.split("\n") if "bench-ns-" in l]
    if remaining:
        log(f"Still {len(remaining)} bench namespaces, waiting 60s more...")
        time.sleep(60)

    time.sleep(informer_settle)
    phases.append(refresh_dashboard(token, "Phase 0: clean slate"))

    # ── Phase 1: create first bench namespace ─────────────────────────────────
    log("Phase 1: creating bench-ns-01...")
    kubectl("create", "ns", "bench-ns-01")
    time.sleep(informer_settle)
    phases.append(refresh_dashboard(token, "Phase 1: +1 namespace"))

    # ── Phase 2: create CompositionDefinition ─────────────────────────────────
    log("Phase 2: creating CompositionDefinition...")
    kubectl("apply", "-f", "-", input_data=COMPDEF_YAML)
    wait_for_compdef_ready(timeout=300)
    time.sleep(informer_settle)
    phases.append(refresh_dashboard(token, "Phase 2: +compdef"))

    # ── Phase 3: create remaining 19 bench namespaces ─────────────────────────
    log(f"Phase 3: creating bench-ns-02 through bench-ns-{BENCH_NS_COUNT:02d}...")
    for i in range(2, BENCH_NS_COUNT + 1):
        kubectl("create", "ns", f"bench-ns-{i:02d}")
    time.sleep(informer_settle)
    phases.append(refresh_dashboard(token, f"Phase 3: +{BENCH_NS_COUNT - 1} namespaces"))

    # ── Phase 4: create 20 compositions ───────────────────────────────────────
    log(f"Phase 4: creating {BENCH_NS_COUNT} compositions...")
    for i in range(1, BENCH_NS_COUNT + 1):
        ns = f"bench-ns-{i:02d}"
        name = f"bench-app-{i:02d}"
        kubectl("apply", "-f", "-", input_data=composition_yaml(ns, name))
    log("All composition apply requests sent, waiting 30s for reconcile...")
    time.sleep(30)
    phases.append(refresh_dashboard(token, f"Phase 4: +{BENCH_NS_COUNT} compositions"))

    # ── Phase 5: delete 1 composition ─────────────────────────────────────────
    log("Phase 5: deleting bench-app-20 in bench-ns-20...")
    kubectl("delete", f"{COMP_RES}.{COMP_GVR}", "bench-app-20",
            "-n", "bench-ns-20", "--ignore-not-found", "--wait=false")
    log("Waiting 15s for informer DELETE event...")
    time.sleep(15)
    phases.append(refresh_dashboard(token, "Phase 5: -1 composition"))

    return phases


