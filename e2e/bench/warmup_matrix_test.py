def log(msg):
    ts = time.strftime("%H:%M:%S")
    print(f"  {DIM}[{ts}]{RESET} {msg}", flush=True)


def section(title):
    print(f"\n{BOLD}{CYAN}{SEP}{RESET}")
    print(f"{BOLD}{CYAN}  {title}{RESET}")
    print(f"{BOLD}{CYAN}{SEP}{RESET}")


def login(username, password):
    """Login via authn basic/login.  This also creates {username}-clientconfig."""
    creds = base64.b64encode(f"{username}:{password}".encode()).decode()
    req = urllib.request.Request(
        AUTHN + "/basic/login",
        headers={"Authorization": "Basic " + creds},
    )
    with urllib.request.urlopen(req, timeout=30) as r:
        return json.load(r)["accessToken"]


def http_get_ms(path, token, base_url=None, timeout=60):
    url = (base_url or SNOWPLOW) + path
    req = urllib.request.Request(
        url,
        headers={"Authorization": "Bearer " + token},
    )
    t0 = time.perf_counter()
    code = 0
    try:
        with urllib.request.urlopen(req, timeout=timeout) as r:
            r.read()
            code = r.status
    except urllib.error.HTTPError as e:
        code = e.code
    except Exception:
        pass
    return int((time.perf_counter() - t0) * 1000), code


def ui_get_ms(path, token, timeout=60):
    """GET a UI page (HTML) through the frontend.  Passes the JWT as a cookie
    (krateo-token) so the UI proxy can forward authenticated API calls."""
    url = UI_URL + path
    req = urllib.request.Request(url, headers={
        "Authorization": "Bearer " + token,
        "Cookie": f"krateo-token={token}",
    })
    t0 = time.perf_counter()
    code = 0
    try:
        with urllib.request.urlopen(req, timeout=timeout) as r:
            r.read()
            code = r.status
    except urllib.error.HTTPError as e:
        code = e.code
    except Exception:
        pass
    return int((time.perf_counter() - t0) * 1000), code


def pct(data, p):
    s = sorted(data)
    return s[max(0, int(round(p / 100.0 * len(s))) - 1)]


def kubectl(*args, input_data=None):
    proc = subprocess.run(
        ["kubectl"] + list(args),
        input=input_data.encode() if input_data else None,
        capture_output=True,
    )
    return proc.returncode, proc.stdout.decode().strip(), proc.stderr.decode().strip()


def wait_for_snowplow(extra_warmup=0):
    log("Waiting for snowplow health endpoint...")
    for _ in range(90):
        try:
            with urllib.request.urlopen(SNOWPLOW + "/health", timeout=5):
                log("Snowplow healthy")
                if extra_warmup > 0:
                    log(f"Sleeping {extra_warmup}s for L3 warmup to complete...")
                    time.sleep(extra_warmup)
                return True
        except Exception:
            time.sleep(2)
    log("ERROR: snowplow not ready after 180s")
    return False


def wait_for_l1_warmup():
    log("Waiting for L1 warmup to complete (polling logs)...")
    deadline = time.time() + 120
    while time.time() < deadline:
        rc, out, _ = kubectl(
            "logs", "deployment/snowplow", "-n", NS,
            "-c", "snowplow", "--tail=300")
        if "L1 warmup: completed" in out:
            log("L1 warmup completed (found in logs)")
            return True
        if "L1 warmup: skipped" in out or "L1 warmup: no users found" in out:
            log("L1 warmup skipped (no users / no GVRs)")
            return True
        time.sleep(5)
    log("WARNING: L1 warmup completion not detected within 120s")
    return False


# ── Environment manipulation ─────────────────────────────────────────────────

def get_clientconfig_secrets():
    rc, out, _ = kubectl("get", "secrets", "-n", NS, "-o", "name")
    return [s.replace("secret/", "") for s in out.split("\n")
            if s.strip() and "-clientconfig" in s]


def delete_all_clientconfig_secrets():
    secrets = get_clientconfig_secrets()
    for name in secrets:
        kubectl("delete", "secret", name, "-n", NS, "--ignore-not-found")
    log(f"Deleted {len(secrets)} clientconfig secrets: {secrets}")


def generate_clientconfigs():
    """Login as every user — the authn service creates *-clientconfig secrets."""
    log("Generating clientconfig secrets by logging in as each user...")
    for username, password in USERS.items():
        try:
            login(username, password)
            log(f"  {username}-clientconfig generated (login OK)")
        except Exception as e:
            log(f"  WARNING: {username} login failed: {e}")
    time.sleep(2)
    created = get_clientconfig_secrets()
    log(f"Clientconfig secrets now present: {created}")


def disable_cache():
    log("Removing Redis sidecar to disable cache...")
    kubectl("patch", "deployment", "snowplow", "-n", NS,
            "--type=json",
            '-p=[{"op":"remove","path":"/spec/template/spec/initContainers"}]')
    kubectl("rollout", "status", "deployment/snowplow", "-n", NS,
            "--timeout=180s")
    wait_for_snowplow()
    time.sleep(3)
    log("Cache disabled (no Redis)")


def enable_cache():
    log("Restoring Redis sidecar...")
    patch = json.dumps({
        "spec": {"template": {"spec": {"initContainers": [{
            "name": "redis",
            "image": "redis:7-alpine",
            "restartPolicy": "Always",
            "ports": [{"containerPort": 6379}],
            "readinessProbe": {
                "exec": {"command": ["redis-cli", "ping"]},
                "initialDelaySeconds": 2, "periodSeconds": 3,
            },
            "resources": {
                "requests": {"cpu": "50m", "memory": "64Mi"},
                "limits": {"cpu": "200m", "memory": "128Mi"},
            },
        }]}}},
    })
    kubectl("patch", "deployment", "snowplow", "-n", NS,
            "--type=strategic", "-p", patch)
    kubectl("rollout", "status", "deployment/snowplow", "-n", NS,
            "--timeout=180s")
    wait_for_snowplow()
    time.sleep(5)
    log("Cache enabled (Redis sidecar restored)")


def restart_snowplow():
    kubectl("rollout", "restart", "deployment/snowplow", "-n", NS)
    kubectl("rollout", "status", "deployment/snowplow", "-n", NS,
            "--timeout=180s")


# ── Benchmark runner ──────────────────────────────────────────────────────────

def run_scenario(label, cache_on, has_clientconfig):
    """Run one A/B/C/D scenario.  Returns {cold: {...}, warm: {...}}."""
    section(f"Scenario {label}")

    # 1. Set up clientconfig secrets
    if has_clientconfig:
        generate_clientconfigs()
    else:
        delete_all_clientconfig_secrets()

    # 2. Set up cache
    if cache_on:
        enable_cache()
    else:
        disable_cache()

    # 3. Restart to pick up changes
    restart_snowplow()
    if not wait_for_snowplow(extra_warmup=5 if cache_on else 0):
        return None

    # 4. Wait for L1 warmup if applicable
    if cache_on and has_clientconfig:
        wait_for_l1_warmup()

    # 5. Login (this also re-creates clientconfig if has_clientconfig,
    #    but after restart the L1 warmup has already run with the pre-existing secrets)
    log("Logging in as admin...")
    try:
        token = login("admin", USERS["admin"])
        log("JWT acquired")
    except Exception as e:
        log(f"ERROR: login failed: {e}")
        return None

    # 6. Cold latency — backend (direct snowplow)
    log("Measuring cold latency — backend (direct snowplow)...")
    cold = {}
    for ep_label, path in ENDPOINTS:
        ms, code = http_get_ms(path, token)
        cold[ep_label] = ms
        icon = GREEN + "✓" + RESET if code == 200 else RED + "✗" + RESET
        log(f"  {icon} COLD  {ep_label:<25s}  {ms:>5d}ms  HTTP {code}")

    # 7. Cold latency — UI navigation
    log("Measuring cold latency — UI pages...")
    ui_cold = {}
    for ep_label, path in UI_PAGES:
        ms, code = ui_get_ms(path, token)
        ui_cold[ep_label] = ms
        icon = GREEN + "✓" + RESET if 200 <= code < 400 else RED + "✗" + RESET
        log(f"  {icon} COLD  {ep_label:<25s}  {ms:>5d}ms  HTTP {code}")

    # 8. Warm latency — backend
    log(f"Measuring warm latency — backend ({ITERS} iterations)...")
    warm = {}
    for ep_label, path in ENDPOINTS:
        http_get_ms(path, token)
        http_get_ms(path, token)
        latencies = []
        for _ in range(ITERS):
            ms, _ = http_get_ms(path, token)
            latencies.append(ms)
        p50 = pct(latencies, 50)
        p90 = pct(latencies, 90)
        mean = round(statistics.mean(latencies))
        warm[ep_label] = {"p50": p50, "p90": p90, "mean": mean}
        log(f"       WARM  {ep_label:<25s}  p50={p50:>5d}ms  p90={p90:>5d}ms  mean={mean:>5d}ms")

    # 9. Warm latency — UI navigation
    log(f"Measuring warm latency — UI pages ({ITERS} iterations)...")
    ui_warm = {}
    for ep_label, path in UI_PAGES:
        ui_get_ms(path, token)
        ui_get_ms(path, token)
        latencies = []
        for _ in range(ITERS):
            ms, _ = ui_get_ms(path, token)
            latencies.append(ms)
        p50 = pct(latencies, 50)
        p90 = pct(latencies, 90)
        mean = round(statistics.mean(latencies))
        ui_warm[ep_label] = {"p50": p50, "p90": p90, "mean": mean}
        log(f"       WARM  {ep_label:<25s}  p50={p50:>5d}ms  p90={p90:>5d}ms  mean={mean:>5d}ms")

    return {"cold": cold, "warm": warm, "ui_cold": ui_cold, "ui_warm": ui_warm}


